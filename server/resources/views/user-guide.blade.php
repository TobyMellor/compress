                        The most recent articles are at the top.<br /><br />
                        If you want to save an article for later, you can click on the <i class="fa fa-heart"></i> next to the Article's header.<br /><br />
                        That article will now be placed in "Read Later", which you can read more about below.<br /><br />
                        To click into an Article, you can click anywhere on the Article.<br /><br />
                        A article in full screen shows you the short article summary, which is a 3 sentence summary of the article's body.<br /><br />
                        Clicking on <span class="android-button">READ EVEN MORE</span> expands the short article summary into 7 sentences.<br /><br />
                        Clicking on the article's title at any point (or <span class="android-button">READ FULL ARTICLE</span> after the above) will take you to the full article on the News Outlet's website.<br /><br />
                        You can exit the full article at any point by clicking the floating <i class="fa fa-times"></i> in the top right.
                    </p>
