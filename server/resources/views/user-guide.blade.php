                        <span class="android-button">Change my news genres</span><br />
                        Takes you back to the second setup screen where you must choose at least 3 Genres.<br /><br />
                        <span class="android-button">Clear read later</span><br />
                        This option will remove all of your articles from the Read Later tab.<br /><br />
                        <span class="android-button">Clear cached articles</span><br />
                        To speed up the apps loading times, we save articles on your device so they don't have to be loaded again.<br /><br />
                        This option will remove articles on your device, and they'll be loaded again when you go to Discover.<br /><br />
