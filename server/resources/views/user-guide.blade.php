                <h1>Read Later</h1>
                <div class="short-summary">
                    <p>
                        The "Read Later" tab is useful when you may not have time to read an Article that catches your attention in Discover, and you want to make sure you don't miss it when you return to the app.<br /><br />
                        Articles here will be persistent forever, until you delete them.
                    </p>
                </div>
                <div class="long-summary">
                    <p>
                        Articles can be added to the Read Later tab by clicking the <i class="fa fa-heart"></i> on articles in Discover.<br /><br />
                        To delete an Article from Read Later, simply click on the <i class="fa fa-trash"></i>.<br /><br />
                        Articles in this tab can be clicked on like any other in Discover. For a full list of available actions when an article is in full screen, please check our the Discover section.<br /><br />
                        If an article is in this section, it means that they will not be present in the Discover tab.
